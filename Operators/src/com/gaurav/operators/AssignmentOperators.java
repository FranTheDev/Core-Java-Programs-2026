package com.gaurav.operators;

public class AssignmentOperators {

    public static void main(String[] args) {

        // Declare and initialize variables
        int a = 10;
        int b = 20;

        // Addition assignment
        a += b;   // a = a + b
        System.out.println("Addition Assignment : " + a);

        // Reset value of a
        a = 10;

        // Subtraction assignment
        a -= b;   // a = a - b
        System.out.println("Subtraction Assignment : " + a);

        // Reset value of a
        a = 10;

        // Multiplication assignment
        a *= b;   // a = a * b
        System.out.println("Multiplication Assignment : " + a);

        // Reset value of a
        a = 10;

        // Division assignment
        a /= b;   // a = a / b
        System.out.println("Division Assignment : " + a);

        // Reset value of a
        a = 10;

        // Modulus assignment
        a %= b;   // a = a % b
        System.out.println("Modulus Assignment : " + a);
    }
}
