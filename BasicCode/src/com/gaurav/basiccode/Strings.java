package com.gaurav.basiccode;

public class Strings 
{

    public static void main(String[] args) 
    {

        char singleChar = 'A';
        // Single quotes → used for single character (char)

        String text = "Gaurav Patil";
        // Double quotes → used for String (multiple characters)

        // Printing output
        System.out.println("Single character : " + singleChar);
        System.out.println("String value : " + text);
    }
}