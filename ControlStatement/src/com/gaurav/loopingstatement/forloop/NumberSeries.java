package com.gaurav.loopingstatement.forloop;

public class NumberSeries 
{
	public static void main(String[] args)
	{
		System.out.println("Number Series Sequential\n");
		
		for(int i=1; i<=10; i++)
		{
			System.out.println(i);
		}
		
		System.out.println("\nNumber Series Reverse\n");
		
		for(int i=10; i>=1; i--)
		{
			System.out.println(i);
		}
	}
}
